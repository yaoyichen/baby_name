def build() -> None:
    # 去重：S 列表先行，A 列表去掉已在 S 中的字
    seen: set[str] = set()
    s_list: list[str] = []
    for ch in S_RAW:
        if ch not in seen:
            seen.add(ch)
            s_list.append(ch)

    a_list: list[str] = []
    for ch in A_RAW:
        if ch not in seen:
            seen.add(ch)
            a_list.append(ch)

    # 验证覆盖率（若 all_chars.json 已存在）
    pool_set: set[str] = set()
    if _ALL.exists():
        for entry in json.loads(_ALL.read_text(encoding="utf-8")):
            pool_set.add(entry["c"])

    if pool_set:
        s_in = sum(1 for c in s_list if c in pool_set)
        a_in = sum(1 for c in a_list if c in pool_set)
        print(f"S级精选: {len(s_list)} 字，其中 {s_in} 字在字库中（{s_in/len(s_list)*100:.0f}%）")
        print(f"A级良选: {len(a_list)} 字，其中 {a_in} 字在字库中（{a_in/len(a_list)*100:.0f}%）")
        not_in_pool_s = [c for c in s_list if c not in pool_set]
        if not_in_pool_s:
            print(f"  S级不在字库中的字：{''.join(not_in_pool_s)}")
    else:
        print(f"S级精选: {len(s_list)} 字（字库未找到，跳过验证）")
        print(f"A级良选: {len(a_list)} 字（字库未找到，跳过验证）")

    result = {
        "_comment": "汉字起名适用性分层。S=精选，A=良选，其余默认B(普通)。",
        "_sources": "易经50吉字 + 公安部2020-2021新生儿高频字 + 人工精选",
        "S": s_list,
        "A": a_list,
    }

    _OUT.write_text(
        json.dumps(result, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    print(f"✅ 已写入 {_OUT}  (S={len(s_list)}, A={len(a_list)})")


if __name__ == "__main__":
    build()
